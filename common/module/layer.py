import math
import torch
import torch.nn as nn

from common.register import *


def autopad(kernel, padding=None):  # kernel, padding
    # Pad to 'same'
    if padding is None:
        padding = kernel // 2 if isinstance(kernel, int) else [x // 2 for x in kernel]  # auto-pad
    return padding


@cnn_register
class Conv(nn.Module):
    # Standard convolution
    def __init__(self, ci, co, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = nn.Conv2d(ci, co, k, s, autopad(k, p), groups=g, bias=False)
        self.bn = nn.BatchNorm2d(co)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())

    def forward(self, x):
        return self.act(self.bn(self.conv(x)))

    def forward_fuse(self, x):
        return self.act(self.conv(x))


@cnn_register
class DWConv(Conv):
    # Depth-wise convolution class
    def __init__(self, ci, co, k=1, s=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__(ci, co, k, s, g=math.gcd(ci, co), act=act)


@cnn_register
class Focus(nn.Module):
    # Focus wh information into c-space
    def __init__(self, ci, co, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = Conv(ci * 4, co, k, s, p, g, act)
